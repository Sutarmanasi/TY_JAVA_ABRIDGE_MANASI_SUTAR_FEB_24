package com.capgemini.assesments.fourth;

public class Employee {

	int eid;
	String ename;
	String email;
	long phoneno;
	double salary;
	
	public Employee(int eid, String ename, String email, long phoneno, double salary) {
		super();
		this.eid = eid;
		this.ename = ename;
		this.email = email;
		this.phoneno = phoneno;
		this.salary=salary;
	}
}
